/**
 * ARIZE PHOENIX TRACING
 * Logs the AI interaction and sends it to the Phoenix collector.
 */
async function traceAIInteraction(userId, message, context, response, latency, mood, provider) {
  try {
    const traceData = {
      trace_id: `trace_${Date.now()}_${userId}`,
      user_id: userId,
      input: message,
      context: context,
      output: response,
      latency_ms: latency,
      mood: mood?.label,
      provider: provider,
      timestamp: new Date().toISOString()
    };

    // Log locally
    console.log(`[TRACE] AI Response generated by ${provider} in ${latency}ms`);

    // Send to Phoenix (Optional - fails silently if collector is down)
    await axios.post(`${PHOENIX_ENDPOINT}/v1/traces`, traceData).catch(() => { });
  } catch (err) {
    console.error('Tracing Error:', err.message);
  }
}

/**
 * PINECONE RAG RETRIEVAL
 * Searches the knowledge base for top-k relevant fragments.
 */
async function retrieveRAGContext(message) {
  try {
    const pcKey = process.env.PINECONE_API_KEY;
    const indexName = process.env.PINECONE_INDEX_NAME;

    if (!pcKey || pcKey === 'your_pinecone_api_key_here') return null;

    const pc = new Pinecone({ apiKey: pcKey });
    const index = pc.index(indexName);

    // 1. Vectorize the User Question using NVIDIA NIM
    const embResult = await openai.embeddings.create({
      input: message,
      model: "nvidia/nv-embedqa-e5-v5",
      encoding_format: "float",
      input_type: "query",
      truncate: "NONE"
    });
    const vector = embResult.data[0].embedding;

    // 2. Query Pinecone with higher topK for diversity
    const queryResponse = await index.query({
      vector,
      topK: 6,
      includeMetadata: true
    });

    if (queryResponse.matches && queryResponse.matches.length > 0) {
      // Hyper-Optimization: Filter by score threshold (0.7) and sort
      const relevantMatches = queryResponse.matches
        .filter(m => m.score > 0.7)
        .sort((a, b) => b.score - a.score);

      if (relevantMatches.length === 0) return null;

      const bestMatch = relevantMatches[0];
      
      // Group by Source for cleaner LLM injection
      const groupedContext = relevantMatches.map(m => {
        const sourceName = m.metadata.source || m.metadata.title || 'ChittSaathi KB';
        return `[SOURCE: ${sourceName} (Relevance: ${Math.round(m.score * 100)}%)] ${m.metadata.text}`;
      }).join('\n\n---\n\n');

      return {
        context: groupedContext,
        confidence: bestMatch.score,
        sources: [...new Set(relevantMatches.map(m => m.metadata.source || m.metadata.title))]
      };
    }
  } catch (err) {
    console.error('RAG Retrieval Error:', err.message);
  }
  return null;
}

async function classifyEmotionUnlimited(input) {
  try {
    const pcKey = process.env.PINECONE_API_KEY;
    const indexName = process.env.PINECONE_INDEX_NAME;
    if (!pcKey || pcKey === 'your_pinecone_api_key_here') return { isCrisis: false };

    // 1. Clean + Emoji Translation (Hackathon Feature)
    const emojiMap = { '😢': 'sadness', '💔': 'heartbreak', '😞': 'distress', '🆘': 'emergency', '🔪': 'harm' };
    let cleanInput = input.toLowerCase();
    Object.keys(emojiMap).forEach(e => cleanInput = cleanInput.replace(new RegExp(e, 'g'), ` ${emojiMap[e]} `));

    // 2. Embed using NVIDIA NIM
    const embResult = await openai.embeddings.create({
      input: cleanInput,
      model: "nvidia/nv-embedqa-e5-v5",
      encoding_format: "float",
      input_type: "query",
      truncate: "NONE"
    });
    const vector = embResult.data[0].embedding;

    // 3. Pinecone Search across specific critical datasets (INDIC, CLPsych, GoEmotions)
    const pc = new Pinecone({ apiKey: pcKey });
    const index = pc.index(indexName);
    const queryResponse = await index.query({
      vector,
      topK: 5,
      filter: { dataset: { $in: ['indic', 'clpsych', 'goemotions'] } },
      includeMetadata: true
    });

    // 4. Threshold Logic: Trigger if highest match is 'crisis' or from specific datasets
    const INDIC_CRISIS_PHRASES = ['जीना नहीं चाहता', 'मरना चाहता हूँ', 'आत्महत्या', 'not worth living', '108', '104'];
    const hasIndicCrisis = INDIC_CRISIS_PHRASES.some(p => input.includes(p));

    const crisisMatch = queryResponse.matches.find(m => 
      (m.metadata.category === 'crisis' || 
       m.metadata.category === 'mental_health' || 
       m.metadata.source?.includes('CLPsych') || 
       m.metadata.source?.includes('Indic')) && 
      m.score > (hasIndicCrisis ? 0.70 : 0.82)
    );

    const sourceTags = [...new Set(queryResponse.matches.slice(0, 2).map(m => `[${m.metadata.source || m.metadata.title}]`))].join(' + ');

    return {
      isCrisis: !!crisisMatch || hasIndicCrisis,
      confidence: crisisMatch?.score || (hasIndicCrisis ? 0.95 : 0),
      status: `High conf ${sourceTags}`,
      dominantCategory: queryResponse.matches[0]?.metadata?.category
    };
  } catch (err) {
    console.error('Semantic Analysis Error:', err.message);
    return { isCrisis: false };
  }
}

// @desc    Generate AI chat response
// @route   POST /api/ai/chat
// @access  Private
const generateChatResponse = async (req, res) => {
  const startTime = Date.now();
  try {
    const { message, context, mood } = req.body;
    const userId = req.user?.id || 'anonymous';

    // 1. Validate and Sanitize Input
    if (!message || typeof message !== 'string' || message.trim().length === 0) {
      return res.status(400).json({ success: false, message: 'Message content is required' });
    }

    // 2. Prevent payload abuse
    const sanitizedMessage = message.trim().substring(0, 2000);
    console.log(`[AI Request] User: ${userId} | Mood: ${mood?.label || 'N/A'}`);

    // --- PHASE 3: UNLIMITED SEMANTIC CRISIS DETECTION ---
    const semanticResult = await classifyEmotionUnlimited(sanitizedMessage);
    const isCrisis = checkEmergencyKeywords(sanitizedMessage) || semanticResult.isCrisis;
    
    console.log(`[Safety] Semantic Check: ${semanticResult.status} | Score: ${semanticResult.confidence}`);

    if (isCrisis) {
      console.log('🚨 SEMANTIC CRISIS DETECTED! Confidence:', semanticResult.confidence);
      const emergencyResponse = getEmergencyResponse();
      await saveConversation(userId, sanitizedMessage, emergencyResponse, 'emergency');
      return res.status(200).json({ 
        success: true, 
        response: emergencyResponse, 
        type: 'emergency',
        semanticConfidence: semanticResult.confidence
      });
    }

    // --- PHASE 2: RAG DATA RETRIEVAL ---
    console.log('Retrieving RAG context...');
    const ragResult = await retrieveRAGContext(sanitizedMessage);
    const ragContext = ragResult?.context || null;

    // --- PHASE 1: PREPARE PROMPT ---
    const aiContext = prepareAIContext(sanitizedMessage, context, mood, ragContext);

    let aiResponse;
    let aiProvider = 'nvidia-nim';

    try {
      aiResponse = await generateNvidiaResponse(aiContext, sanitizedMessage);
    } catch (aiError) {
      console.warn('NVIDIA NIM failed, trying Gemma Fallback...');
      try {
        aiResponse = await generateGemmaFallback(aiContext, sanitizedMessage);
        aiProvider = 'gemma';
      } catch (gemmaError) {
        aiResponse = generateFallbackResponse(sanitizedMessage, mood);
        aiProvider = 'rule-based';
      }
    }

    let safeResponse = applySafetyFilters(aiResponse);
    
    // --- PHASE 4: RAG CITATIONS ---
    if (aiProvider === 'nvidia-nim' && ragResult && ragContext) {
      const confidencePercent = Math.round((ragResult.confidence || 0) * 100);
      const sourcesStr = (ragResult.sources || []).join(', ');
      safeResponse += `\n\n📚 Sources: [${sourcesStr || 'ChittSaathi KB'}] | Confidence: ${confidencePercent}%`;
    }

    const latency = Date.now() - startTime;

    // --- PHASE 2: ARIZE TRACING ---
    await traceAIInteraction(userId, sanitizedMessage, aiContext, safeResponse, latency, mood, aiProvider);

    await saveConversation(userId, sanitizedMessage, safeResponse, 'normal');

    res.status(200).json({
      success: true,
      response: safeResponse,
      type: aiProvider,
      mood: mood?.label || null,
      latency: `${latency}ms`,
      rag: {
        used: !!ragContext,
        confidence: ragContext?.confidence >= 0.7 ? "High" : "Standard",
        sources: ragContext?.sources || []
      }
    });

  } catch (error) {
    console.error('AI Chat Error:', error);
    res.status(500).json({ success: false, message: 'Failed to generate response' });
  }
};

// @desc    Get conversation history
// @route   GET /api/ai/conversation
// @access  Private
const getConversationHistory = async (req, res) => {
  try {
    const userId = req.user.id;
    const limit = parseInt(req.query.limit) || 50;

    const conversations = await Conversation.find({ user: userId })
      .sort({ createdAt: -1 })
      .limit(limit)
      .select('userMessage aiResponse type createdAt');

    res.status(200).json({
      success: true,
      conversations: conversations.reverse() // Return in chronological order
    });

  } catch (error) {
    console.error('Get Conversation Error:', error);
    res.status(500).json({
      success: false,
      message: 'Failed to retrieve conversation history'
    });
  }
};

