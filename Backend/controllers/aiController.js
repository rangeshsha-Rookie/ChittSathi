// Function to initialize AI service with better error handling
function initializeAI() {
  try {
    const nvKey = process.env.NVIDIA_API_KEY;

    console.log('Checking NVIDIA API Key...');
    console.log('API Key exists:', !!nvKey);
    console.log('API Key preview:', nvKey ? `${nvKey.substring(0, 10)}...` : 'No key found');

    if (!nvKey || nvKey === 'your_nvidia_nim_api_key_here') {
      console.warn('🚨 No NVIDIA API key provided. AI responses will use fallback mode.');
      return false;
    }

    openai = new OpenAI({
      apiKey: nvKey.trim(),
      baseURL: 'https://integrate.api.nvidia.com/v1',
    });
    console.log('✅ NVIDIA NIM initialized successfully with API key');
    return true;
  } catch (error) {
    console.error('❌ Failed to initialize NVIDIA NIM:', error.message);
    return false;
  }
}

